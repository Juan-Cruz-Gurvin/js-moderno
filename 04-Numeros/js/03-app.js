let resultado;

// PI
resultado = Math.PI;
console.log(resultado);

// Redondear
resultado = Math.round(2.8);
console.log(resultado);
resultado = Math.round(3.5);
console.log(resultado);
resultado = Math.round(4.3);
console.log(resultado);

// Redondear hacia arriba
resultado = Math.ceil(5.1);
console.log(resultado);

// Redondea hacia abajo
resultado = Math.floor(2.9);
console.log(resultado);

// Raíz cuadrada
resultado = Math.sqrt(144);
console.log(resultado);

// Valor Absoluto
resultado = Math.abs(-300);
console.log(resultado);

// Potencia
resultado = Math.pow(2, 4);
console.log(resultado);

// Minimo
resultado = Math.min(3, 4, 8, 2, 1)
console.log(resultado);

// Máximo
resultado = Math.max(3, 4, 8, 2, 1)
console.log(resultado);

// Aleatorio
resultado = Math.random(); // No se recomienda mucho
console.log(resultado);

// Aleatorio dentro de un rango..bundleRenderer.renderToStream
resultado = Math.floor( Math.random() * 30 ); // rango del 0 al 30
